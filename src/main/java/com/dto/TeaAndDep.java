package com.dto;

public class TeaAndDep {
    private String name;
    private String password;
    private Integer number;
    private String title;
    private String phone;
    private Integer assessor;
    private Integer teaDepNum;
    private String teaJob;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public Integer getAssessor() {
        return assessor;
    }

    public void setAssessor(Integer assessor) {
        this.assessor = assessor;
    }

    public Integer getTeaDepNum() {
        return teaDepNum;
    }

    public void setTeaDepNum(Integer teaDepNum) {
        this.teaDepNum = teaDepNum;
    }

    public String getTeaJob() {
        return teaJob;
    }

    public void setTeaJob(String teaJob) {
        this.teaJob = teaJob;
    }


}
